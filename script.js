function showBar(){
  document.getElementById("nav_bar").style.display = "block";
  document.getElementById("close_menu").style.display = "block";
  document.getElementById("menu_non").style.display = "none";
}

function closeBar(){
  document.getElementById("nav_bar").style.display = "none";
  document.getElementById("close_menu").style.display = "none";
  document.getElementById("menu_non").style.display = "block";
}